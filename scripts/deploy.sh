counter=0
until docker-compose -f $COMPOSE_FILE exec -T postgres pg_isready -U mywebsite > /dev/null 2>&1; do
    sleep 2
    counter=$((counter + 2))
    if [ $counter -ge $timeout ]; then
        echo "‚ùå PostgreSQL –Ω–µ –∑–∞–ø—É—Å—Ç–∏–ª—Å—è –∑–∞ $timeout —Å–µ–∫—É–Ω–¥"
        exit 1
    fi
