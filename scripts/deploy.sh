    echo "‚è≠Ô∏è  –ü—Ä–æ–ø—É—Å–∫ —Å–±–æ—Ä–∫–∏ (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è --no-build)"
fi

# –ó–∞–ø—É—Å–∫–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã
